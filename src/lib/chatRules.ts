//    export const BANNED_WORDS: string[] = ["كلمة1", "كلمة2", "كلمة3"]
export const BANNED_WORDS: string[] = []

// 2) ساعات الهدوء الليلي لمجتمع المعرفة (بتوقيت جهاز التلميذ)
export const QUIET_START = 21 // يُقفل ابتداء من التاسعة ليلاً
export const QUIET_END = 7    // يُفتح على السابعة صباحاً

export function isQuietHours(): boolean {
  const h = new Date().getHours()
  return h >= QUIET_START || h < QUIET_END
}

export function containsBanned(text: string): boolean {
  return BANNED_WORDS.some(w => w.trim() !== '' && text.includes(w))
}

